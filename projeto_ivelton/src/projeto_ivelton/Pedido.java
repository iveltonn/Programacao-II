

package projeto_ivelton;

public class Pedido {
	 private String item;
	    private double preco;

	    public Pedido(String item, double preco) {
	        this.item = item;
	        this.preco = preco;
	    }

	    public String getItem() {
	        return item;
	    }

	    public double getPreco() {
	        return preco;
	    }

	    
}
